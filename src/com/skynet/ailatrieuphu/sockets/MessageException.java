package com.skynet.ailatrieuphu.sockets;

public class MessageException extends Exception {

    private static final long serialVersionUID = 1113328621571829372L;

    public MessageException() {
        super();

    }

    public MessageException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

    public MessageException(String arg0) {
        super(arg0);

    }

    public MessageException(Throwable arg0) {
        super(arg0);

    }

}
